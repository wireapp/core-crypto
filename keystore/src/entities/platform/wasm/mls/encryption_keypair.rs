#[derive(serde::Serialize)]
pub struct StoredEncryptionKeyPairEncrypt<'a> {
    // borrowed fields
    pk: &'a [u8],
    // encrypted fields
    sk: Vec<u8>,
}

impl<'a> Encrypting<'a> for StoredEncryptionKeyPair {
    type EncryptedForm = StoredEncryptionKeyPairEncrypt<'a>;

    fn encrypt(&'a self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::EncryptedForm> {
        let sk = <StoredEncryptionKeyPair as EncryptData>::encrypt_data(self, cipher, &self.sk)?;

        Ok(StoredEncryptionKeyPairEncrypt { pk: &self.pk, sk })
    }
}

/// The encrypted form of a stored credential, which can be decrypted
#[derive(serde::Deserialize)]
pub struct StoredEncryptionKeyPairDecrypt {
    pk: Vec<u8>,
    sk: Vec<u8>,
}

impl Decrypting<'static> for StoredEncryptionKeyPairDecrypt {
    type DecryptedForm = StoredEncryptionKeyPair;

    fn decrypt(self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::DecryptedForm> {
        let Self { pk, sk } = self;

        let sk = <StoredEncryptionKeyPair as DecryptData>::decrypt_data(cipher, &pk, &sk)?;

        Ok(StoredEncryptionKeyPair { pk, sk })
    }
}

impl Decryptable<'static> for StoredEncryptionKeyPair {
    type DecryptableFrom = StoredEncryptionKeyPairDecrypt;
}
