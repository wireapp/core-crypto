import {
    createPkiEnvironment,
    PkiEnvironment as PkiEnvironmentInternal,
    type PkiEnvironmentHooks,
} from "../browser/autogenerated/core_crypto_ffi";
import type { Database } from "./Database";

export class PkiEnvironment extends PkiEnvironmentInternal {
    /** @internal */
    constructor(pkiEnvironment: PkiEnvironment) {
        super(pkiEnvironment);
    }

    public static async new(
        hooks: PkiEnvironmentHooks,
        database: Database
    ): Promise<PkiEnvironment> {
        const instance = await createPkiEnvironment(hooks, database);
        // this class inherits the passthrough constructor from `*Internal`, so calling it here
        // produces a Self instance instead of `*Internal`.
        // they're structurally equivalent, but technically have distinct types.
        return new PkiEnvironment(instance);
    }

    // We have to override this method in order to have no references
    // to the internal database type in our documentation.
    static instanceOf(obj: unknown): obj is PkiEnvironment {
        return super.instanceOf(obj);
    }
}
