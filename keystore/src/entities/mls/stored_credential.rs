use zeroize::Zeroize;

use crate::{Sha256Hash, traits::KeyType};

/// This type exists so that we can efficiently search for credentials by a variety of metrics at the database level.
///
/// This includes some but not all of the fields in `core_crypto::CredentialFindFilters`: those that are actually stored
/// in the database, and do not require deserializing the `credential` field.
#[derive(Debug, Default, Clone, Copy, serde::Serialize)]
pub struct CredentialFindFilters<'a> {
    /// Hash of public key to search for.
    pub hash: Option<Sha256Hash>,
    /// Public key to search for
    pub public_key: Option<&'a [u8]>,
    /// Session / Client id to search for
    pub session_id: Option<&'a [u8]>,
    /// Ciphersuite to search for
    pub ciphersuite: Option<u16>,
    /// unix timestamp (seconds) of point of earliest validity to search for
    pub earliest_validity: Option<u64>,
}

impl<'a> KeyType for CredentialFindFilters<'a> {
    fn bytes(&self) -> std::borrow::Cow<'_, [u8]> {
        postcard::to_stdvec(self)
            .expect("serializing these filters cannot fail")
            .into()
    }
}

/// Entity representing a persisted `Credential`
#[derive(core_crypto_macros::Debug, Clone, PartialEq, Eq, Zeroize, serde::Serialize, serde::Deserialize)]
#[zeroize(drop)]
pub struct StoredCredential {
    /// Note: this is not a unique identifier, but the session id this credential belongs to.
    #[sensitive]
    pub session_id: Vec<u8>,
    #[sensitive]
    pub credential: Vec<u8>,
    pub created_at: u64,
    pub ciphersuite: u16,
    #[sensitive]
    pub public_key: Vec<u8>,
    #[sensitive]
    pub private_key: Vec<u8>,
}

impl crate::traits::UnifiedEntity for StoredCredential {
    const COLLECTION_NAME: &'static str = "mls_credentials";

    fn get(conn: &rusqlite::Connection, key: &crate::Sha256Hash) -> crate::CryptoKeystoreResult<Option<Self>> {
        use rusqlite::OptionalExtension as _;
        let mut stmt = conn.prepare_cached(
            "SELECT session_id, credential, unixepoch(created_at) AS created_at, ciphersuite, public_key, private_key \
             FROM mls_credentials WHERE public_key_sha256 = ?",
        )?;
        stmt.query_row([key], |row| {
            Ok(Self {
                session_id: row.get("session_id")?,
                credential: row.get("credential")?,
                created_at: row.get("created_at")?,
                ciphersuite: row.get("ciphersuite")?,
                public_key: row.get("public_key")?,
                private_key: row.get("private_key")?,
            })
        })
        .optional()
        .map_err(Into::into)
    }

    fn count(conn: &rusqlite::Connection) -> crate::CryptoKeystoreResult<u32> {
        crate::entities::helpers::count_helper::<Self>(conn)
    }

    fn load_all(conn: &rusqlite::Connection) -> crate::CryptoKeystoreResult<Vec<Self>> {
        let mut stmt = conn.prepare_cached(
            "SELECT session_id, credential, unixepoch(created_at) AS created_at, ciphersuite, public_key, private_key \
             FROM mls_credentials",
        )?;
        stmt.query_map([], |row| {
            Ok(Self {
                session_id: row.get("session_id")?,
                credential: row.get("credential")?,
                created_at: row.get("created_at")?,
                ciphersuite: row.get("ciphersuite")?,
                public_key: row.get("public_key")?,
                private_key: row.get("private_key")?,
            })
        })?
        .collect::<Result<_, _>>()
        .map_err(Into::into)
    }
}

impl crate::traits::UnifiedEntityDatabaseMutation for StoredCredential {
    type AutoGeneratedFields = u64;

    fn pre_save(&mut self) -> crate::CryptoKeystoreResult<Self::AutoGeneratedFields> {
        let created_at = std::time::SystemTime::now()
            .duration_since(std::time::SystemTime::UNIX_EPOCH)
            .map_err(|_| crate::CryptoKeystoreError::TimestampError)?
            .as_secs();
        self.created_at = created_at;
        Ok(created_at)
    }

    fn save(&self, tx: &rusqlite::Transaction) -> crate::CryptoKeystoreResult<()> {
        use crate::traits::PrimaryKey as _;
        let mut stmt = tx.prepare_cached(
            "INSERT INTO mls_credentials \
             (public_key_sha256, public_key, session_id, credential, created_at, ciphersuite, private_key) \
             VALUES \
             (:public_key_sha256, :public_key, :session_id, :credential, datetime(:created_at, 'unixepoch'), :ciphersuite, :private_key)",
        )?;
        stmt.execute(rusqlite::named_params![
            ":public_key_sha256": self.primary_key(),
            ":public_key": self.public_key,
            ":session_id": self.session_id,
            ":credential": self.credential,
            ":created_at": self.created_at,
            ":ciphersuite": self.ciphersuite,
            ":private_key": self.private_key,
        ])?;
        Ok(())
    }

    fn count(tx: &rusqlite::Transaction) -> crate::CryptoKeystoreResult<u32> {
        crate::entities::helpers::count_helper_tx::<Self>(tx)
    }

    fn delete(tx: &rusqlite::Transaction, id: &crate::Sha256Hash) -> crate::CryptoKeystoreResult<bool> {
        crate::entities::helpers::delete_helper::<Self>(tx, "public_key_sha256", *id)
    }
}

impl<'a> crate::traits::UnifiedSearchableEntity<CredentialFindFilters<'a>> for StoredCredential {
    fn find_all_matching(
        conn: &rusqlite::Connection,
        filters: &CredentialFindFilters<'a>,
    ) -> crate::CryptoKeystoreResult<Vec<Self>> {
        if let Some(hash) = filters
            .hash
            .or_else(|| filters.public_key.map(crate::Sha256Hash::hash_from))
        {
            return <Self as crate::traits::UnifiedEntity>::get(conn, &hash).map(|opt| {
                opt.into_iter()
                    .filter(|c| <Self as crate::traits::UnifiedSearchableEntity<_>>::matches(c, filters))
                    .collect()
            });
        }

        let mut query =
            "SELECT session_id, credential, unixepoch(created_at) AS created_at, ciphersuite, public_key, private_key \
             FROM mls_credentials \
             WHERE (true OR :ciphersuite OR :created_at OR :session_id) "
                .to_owned();

        if filters.ciphersuite.is_some() {
            query.push_str("AND ciphersuite = :ciphersuite ");
        }
        if filters.earliest_validity.is_some() {
            query.push_str("AND unixepoch(created_at) = :created_at ");
        }
        if filters.session_id.is_some() {
            query.push_str("AND session_id = :session_id ");
        }

        let mut stmt = conn.prepare(&query)?;
        stmt.query_map(
            rusqlite::named_params![
                ":ciphersuite": filters.ciphersuite,
                ":created_at": filters.earliest_validity,
                ":session_id": filters.session_id,
            ],
            |row| {
                Ok(Self {
                    session_id: row.get("session_id")?,
                    credential: row.get("credential")?,
                    created_at: row.get("created_at")?,
                    ciphersuite: row.get("ciphersuite")?,
                    public_key: row.get("public_key")?,
                    private_key: row.get("private_key")?,
                })
            },
        )?
        .collect::<Result<_, _>>()
        .map_err(Into::into)
    }

    fn matches(&self, filters: &CredentialFindFilters<'a>) -> bool {
        use crate::traits::PrimaryKey as _;
        filters.hash.is_none_or(|hash| hash == self.primary_key())
            && filters.public_key.is_none_or(|pk| pk == self.public_key)
            && filters.session_id.is_none_or(|sid| sid == self.session_id)
            && filters.ciphersuite.is_none_or(|cs| cs == self.ciphersuite)
            && filters.earliest_validity.is_none_or(|ev| ev == self.created_at)
    }
}
