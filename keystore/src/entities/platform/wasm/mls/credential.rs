use async_trait::async_trait;
use web_time::SystemTime;

use crate::{
    CryptoKeystoreError, CryptoKeystoreResult, MissingKeyErrorKind, Sha256Hash,
    connection::{DatabaseConnection, KeystoreDatabaseConnection, TransactionWrapper},
    entities::{Entity, EntityBase, EntityFindParams, EntityTransactionExt, StoredCredential, StringEntityId},
    traits::{
        DecryptData, Decryptable, Decrypting, EncryptData, Encrypting, Entity as NewEntity,
        EntityBase as NewEntityBase, EntityDatabaseMutation, KeyType as _,
    },
};

#[async_trait::async_trait(?Send)]
impl EntityBase for StoredCredential {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = u64;
    const COLLECTION_NAME: &'static str = "mls_credentials";

    fn to_missing_key_err_kind() -> MissingKeyErrorKind {
        MissingKeyErrorKind::StoredCredential
    }

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::StoredCredential(self.into())
    }
}

#[async_trait::async_trait(?Send)]
impl EntityTransactionExt for StoredCredential {
    async fn pre_save<'a>(&'a mut self) -> CryptoKeystoreResult<Self::AutoGeneratedFields> {
        let now = SystemTime::now();
        let created_at = now
            .duration_since(SystemTime::UNIX_EPOCH)
            .map_err(|_| CryptoKeystoreError::TimestampError)?
            .as_secs();
        self.created_at = created_at;
        Ok(created_at)
    }
}

#[async_trait::async_trait(?Send)]
impl Entity for StoredCredential {
    fn id_raw(&self) -> &[u8] {
        &self.public_key
    }

    async fn find_all(conn: &mut Self::ConnectionType, params: EntityFindParams) -> CryptoKeystoreResult<Vec<Self>> {
        let storage = conn.storage();
        storage
            .get_all(<Self as EntityBase>::COLLECTION_NAME, Some(params))
            .await
    }

    async fn find_one(
        conn: &mut Self::ConnectionType,
        id: &StringEntityId,
    ) -> crate::CryptoKeystoreResult<Option<Self>> {
        conn.storage()
            .get(<Self as EntityBase>::COLLECTION_NAME, id.as_slice())
            .await
    }

    async fn count(conn: &mut Self::ConnectionType) -> crate::CryptoKeystoreResult<usize> {
        conn.storage().count(<Self as EntityBase>::COLLECTION_NAME).await
    }

    fn merge_key(&self) -> Vec<u8> {
        // Credentials are unique by id and type, the type is contained in the bytes
        // inside self.credential.
        self.id_raw().iter().chain(self.credential.iter()).cloned().collect()
    }

    fn encrypt(&mut self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<()> {
        self.credential = <Self as Entity>::encrypt_data(self, cipher, self.credential.as_slice())?;
        Self::ConnectionType::check_buffer_size(self.credential.len())?;

        Ok(())
    }

    fn decrypt(&mut self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<()> {
        self.credential = self.decrypt_data(cipher, self.credential.as_slice())?;

        Ok(())
    }
}

impl NewEntityBase for StoredCredential {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = u64;
    const COLLECTION_NAME: &'static str = "mls_credentials";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::StoredCredential(self.into())
    }
}

#[async_trait(?Send)]
impl NewEntity for StoredCredential {
    type PrimaryKey = Sha256Hash;

    fn primary_key(&self) -> Self::PrimaryKey {
        Sha256Hash::hash_from(&self.public_key)
    }

    async fn get(conn: &mut Self::ConnectionType, key: &Self::PrimaryKey) -> CryptoKeystoreResult<Option<Self>> {
        conn.storage().new_get(key.bytes().as_ref()).await
    }

    async fn count(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<u32> {
        conn.storage().new_count::<Self>().await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        conn.storage().new_get_all().await
    }
}

#[async_trait(?Send)]
impl<'a> EntityDatabaseMutation<'a> for StoredCredential {
    type Transaction = TransactionWrapper<'a>;

    async fn pre_save(&mut self) -> CryptoKeystoreResult<Self::AutoGeneratedFields> {
        let now = SystemTime::now();
        let created_at = now
            .duration_since(SystemTime::UNIX_EPOCH)
            .map_err(|_| CryptoKeystoreError::TimestampError)?
            .as_secs();
        self.created_at = created_at;
        Ok(created_at)
    }

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        tx.new_save(self).await
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        tx.new_count::<Self>().await
    }

    async fn delete(tx: &Self::Transaction, id: &<Self as NewEntity>::PrimaryKey) -> CryptoKeystoreResult<bool> {
        tx.new_delete::<Self>(id.bytes().as_ref()).await
    }
}

#[derive(serde::Serialize)]
pub struct StoredCredentialEncrypt<'a> {
    // Copy fields
    created_at: u64,
    ciphersuite: u16,
    // borrowed fields
    session_id: &'a [u8],
    public_key: &'a [u8],
    // encrypted fields
    credential: Vec<u8>,
    private_key: Vec<u8>,
}

impl<'a> Encrypting<'a> for StoredCredential {
    type EncryptedForm = StoredCredentialEncrypt<'a>;

    fn encrypt(&'a self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::EncryptedForm> {
        let credential = <StoredCredential as EncryptData>::encrypt_data(self, cipher, &self.credential)?;
        let private_key = <StoredCredential as EncryptData>::encrypt_data(self, cipher, &self.private_key)?;

        Ok(StoredCredentialEncrypt {
            created_at: self.created_at,
            ciphersuite: self.ciphersuite,
            session_id: &self.session_id,
            public_key: &self.public_key,
            credential,
            private_key,
        })
    }
}

/// The encrypted form of a stored credential, which can be decrypted
#[derive(serde::Deserialize)]
pub struct StoredCredentialDecrypt {
    session_id: Vec<u8>,
    credential: Vec<u8>,
    created_at: u64,
    ciphersuite: u16,
    public_key: Vec<u8>,
    private_key: Vec<u8>,
}

impl Decrypting<'static> for StoredCredentialDecrypt {
    type DecryptedForm = StoredCredential;

    fn decrypt(self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::DecryptedForm> {
        let Self {
            session_id,
            credential,
            created_at,
            ciphersuite,
            public_key,
            private_key,
        } = self;

        // we encrypt only two fields here: the serialized form of the credential itself,
        // and the private key
        let primary_key = Sha256Hash::hash_from(&public_key);
        let credential = <StoredCredential as DecryptData>::decrypt_data(cipher, &primary_key, &credential)?;
        let private_key = <StoredCredential as DecryptData>::decrypt_data(cipher, &primary_key, &private_key)?;

        Ok(StoredCredential {
            session_id,
            credential,
            created_at,
            ciphersuite,
            public_key,
            private_key,
        })
    }
}

impl Decryptable<'static> for StoredCredential {
    type DecryptableFrom = StoredCredentialDecrypt;
}
