use zeroize::Zeroize;

#[derive(core_crypto_macros::Debug, Clone, Zeroize, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
#[zeroize(drop)]
#[sensitive]
pub struct ProteusIdentity {
    pub sk: Vec<u8>,
    pub pk: Vec<u8>,
}

impl ProteusIdentity {
    pub const SK_KEY_SIZE: usize = 64;
    pub const PK_KEY_SIZE: usize = 32;
    pub const ID: &[u8; 1] = b"1";

    pub fn sk_raw(&self) -> zeroize::Zeroizing<[u8; Self::SK_KEY_SIZE]> {
        let mut slice = zeroize::Zeroizing::new([0u8; Self::SK_KEY_SIZE]);
        debug_assert_eq!(self.sk.len(), Self::SK_KEY_SIZE);
        slice.copy_from_slice(&self.sk[..Self::SK_KEY_SIZE]);
        slice
    }

    pub fn pk_raw(&self) -> zeroize::Zeroizing<[u8; Self::PK_KEY_SIZE]> {
        let mut slice = zeroize::Zeroizing::new([0u8; Self::PK_KEY_SIZE]);
        debug_assert_eq!(self.pk.len(), Self::PK_KEY_SIZE);
        slice.copy_from_slice(&self.pk[..Self::PK_KEY_SIZE]);
        slice
    }
}

impl crate::traits::UnifiedEntity for ProteusIdentity {
    const COLLECTION_NAME: &'static str = "proteus_identities";

    fn get(conn: &rusqlite::Connection, _key: &Self::PrimaryKey) -> crate::CryptoKeystoreResult<Option<Self>> {
        use rusqlite::OptionalExtension as _;
        let mut stmt = conn.prepare_cached("SELECT sk, pk FROM proteus_identities ORDER BY rowid ASC LIMIT 1")?;
        stmt.query_row([], |row| {
            Ok(Self {
                sk: row.get("sk")?,
                pk: row.get("pk")?,
            })
        })
        .optional()
        .map_err(Into::into)
    }

    fn count(conn: &rusqlite::Connection) -> crate::CryptoKeystoreResult<u32> {
        crate::entities::helpers::count_helper::<Self>(conn)
    }

    fn load_all(conn: &rusqlite::Connection) -> crate::CryptoKeystoreResult<Vec<Self>> {
        crate::entities::helpers::load_all_helper(conn, |row| {
            Ok(Self {
                sk: row.get("sk")?,
                pk: row.get("pk")?,
            })
        })
    }
}

impl crate::traits::UnifiedEntityDatabaseMutation for ProteusIdentity {
    type AutoGeneratedFields = ();

    fn save(&self, tx: &rusqlite::Transaction) -> crate::CryptoKeystoreResult<()> {
        let mut stmt = tx.prepare_cached("INSERT INTO proteus_identities (sk, pk) VALUES (?, ?)")?;
        stmt.execute(rusqlite::params![self.sk, self.pk])?;
        Ok(())
    }

    fn count(tx: &rusqlite::Transaction) -> crate::CryptoKeystoreResult<u32> {
        crate::entities::helpers::count_helper_tx::<Self>(tx)
    }

    fn delete(tx: &rusqlite::Transaction, _id: &Self::PrimaryKey) -> crate::CryptoKeystoreResult<bool> {
        let mut stmt = tx.prepare_cached("DELETE FROM proteus_identities")?;
        let updated = stmt.execute([])?;
        Ok(updated > 0)
    }
}

