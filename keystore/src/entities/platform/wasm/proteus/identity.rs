use async_trait::async_trait;
use serde::{Deserialize, Serialize};

use crate::{
    CryptoKeystoreResult,
    connection::{KeystoreDatabaseConnection, TransactionWrapper},
    entities::ProteusIdentity,
    traits::{
        DecryptData, Decryptable, Decrypting, EncryptData, Encrypting, Entity, EntityBase, EntityDatabaseMutation,
        KeyType as _, PrimaryKey, UniqueEntity,
    },
};

impl EntityBase for ProteusIdentity {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "proteus_identities";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::ProteusIdentity(self.into())
    }
}

impl UniqueEntity for ProteusIdentity {
    const KEY: [u8; 1] = [1];
}

impl PrimaryKey for ProteusIdentity {
    type PrimaryKey = [u8; 1];
    fn primary_key(&self) -> Self::PrimaryKey {
        Self::KEY
    }
}

#[async_trait(?Send)]
impl Entity for ProteusIdentity {
    async fn get(conn: &mut Self::ConnectionType, _key: &Self::PrimaryKey) -> CryptoKeystoreResult<Option<Self>> {
        let identity = Self::load_all(conn).await?.pop();
        Ok(identity)
    }

    async fn count(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<u32> {
        conn.storage().count::<Self>().await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        conn.storage().get_all().await
    }
}

#[async_trait(?Send)]
impl<'a> EntityDatabaseMutation<'a> for ProteusIdentity {
    type Transaction = TransactionWrapper<'a>;

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        tx.save(self).await
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        tx.count::<Self>().await
    }

    async fn delete(tx: &Self::Transaction, id: &Self::PrimaryKey) -> CryptoKeystoreResult<bool> {
        tx.delete::<Self>(id.bytes().as_ref()).await
    }
}

#[derive(Serialize, Deserialize)]
pub struct ProteusIdentityEncrypted<'a> {
    #[serde(skip)]
    _lifetime: std::marker::PhantomData<&'a ()>,
    sk: Vec<u8>,
    pk: Vec<u8>,
}

impl<'a> Encrypting<'a> for ProteusIdentity {
    type EncryptedForm = ProteusIdentityEncrypted<'a>;

    fn encrypt(&self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::EncryptedForm> {
        let sk = <Self as EncryptData>::encrypt_data(self, cipher, &self.sk)?;
        let pk = <Self as EncryptData>::encrypt_data(self, cipher, &self.pk)?;
        Ok(ProteusIdentityEncrypted {
            _lifetime: std::marker::PhantomData,
            sk,
            pk,
        })
    }
}

impl Decrypting<'static> for ProteusIdentityEncrypted<'static> {
    type DecryptedForm = ProteusIdentity;

    fn decrypt(self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::DecryptedForm> {
        let sk = <ProteusIdentity as DecryptData>::decrypt_data(cipher, &ProteusIdentity::KEY, &self.sk)?;
        let pk = <ProteusIdentity as DecryptData>::decrypt_data(cipher, &ProteusIdentity::KEY, &self.pk)?;
        Ok(ProteusIdentity { sk, pk })
    }
}

impl Decryptable<'static> for ProteusIdentity {
    type DecryptableFrom = ProteusIdentityEncrypted<'static>;
}
