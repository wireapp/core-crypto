use async_trait::async_trait;
use serde::{Deserialize, Serialize};

use crate::{
    CryptoKeystoreResult, MissingKeyErrorKind, Sha256Hash,
    connection::{DatabaseConnection, KeystoreDatabaseConnection, TransactionWrapper},
    entities::{Entity, EntityBase, EntityFindParams, EntityTransactionExt, StoredHpkePrivateKey, StringEntityId},
    traits::{
        DecryptData, Decryptable, Decrypting, EncryptData, Encrypting, Entity as NewEntity,
        EntityBase as NewEntityBase, EntityDatabaseMutation, KeyType,
    },
};

#[async_trait::async_trait(?Send)]
impl EntityBase for StoredHpkePrivateKey {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "mls_hpke_private_keys";

    fn to_missing_key_err_kind() -> MissingKeyErrorKind {
        MissingKeyErrorKind::StoredHpkePrivateKey
    }

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::HpkePrivateKey(self.into())
    }
}

#[async_trait::async_trait(?Send)]
impl EntityTransactionExt for StoredHpkePrivateKey {}

#[async_trait::async_trait(?Send)]
impl Entity for StoredHpkePrivateKey {
    fn id_raw(&self) -> &[u8] {
        self.pk.as_slice()
    }

    async fn find_all(conn: &mut Self::ConnectionType, params: EntityFindParams) -> CryptoKeystoreResult<Vec<Self>> {
        let storage = conn.storage();
        storage
            .get_all(<Self as EntityBase>::COLLECTION_NAME, Some(params))
            .await
    }

    async fn find_one(
        conn: &mut Self::ConnectionType,
        id: &StringEntityId,
    ) -> crate::CryptoKeystoreResult<Option<Self>> {
        conn.storage()
            .get(<Self as EntityBase>::COLLECTION_NAME, id.as_slice())
            .await
    }

    async fn count(conn: &mut Self::ConnectionType) -> crate::CryptoKeystoreResult<usize> {
        conn.storage().count(<Self as EntityBase>::COLLECTION_NAME).await
    }

    fn encrypt(&mut self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<()> {
        self.sk = <Self as Entity>::encrypt_data(self, cipher, self.sk.as_slice())?;
        Self::ConnectionType::check_buffer_size(self.sk.len())?;

        Ok(())
    }

    fn decrypt(&mut self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<()> {
        self.sk = self.decrypt_data(cipher, self.sk.as_slice())?;

        Ok(())
    }
}

impl NewEntityBase for StoredHpkePrivateKey {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "mls_hpke_private_keys";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::HpkePrivateKey(self.into())
    }
}

#[async_trait(?Send)]
impl NewEntity for StoredHpkePrivateKey {
    type PrimaryKey = Sha256Hash;

    fn primary_key(&self) -> Sha256Hash {
        Sha256Hash::hash_from(&self.pk)
    }

    async fn get(conn: &mut Self::ConnectionType, id: &Sha256Hash) -> CryptoKeystoreResult<Option<Self>> {
        conn.storage().new_get(id.bytes().as_ref()).await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        conn.storage().new_get_all().await
    }

    async fn count(conn: &mut Self::ConnectionType) -> crate::CryptoKeystoreResult<u32> {
        conn.storage().new_count::<Self>().await
    }
}

#[async_trait(?Send)]
impl<'a> EntityDatabaseMutation<'a> for StoredHpkePrivateKey {
    type Transaction = TransactionWrapper<'a>;

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        tx.new_save(self).await
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        tx.new_count::<Self>().await
    }

    async fn delete(tx: &Self::Transaction, id: &Sha256Hash) -> CryptoKeystoreResult<bool> {
        tx.new_delete::<Self>(id.bytes().as_ref()).await
    }
}

#[derive(Serialize)]
pub struct StoredHpkePrivateKeyEncrypt<'a> {
    sk: Vec<u8>,
    pk: &'a [u8],
}

impl<'a> Encrypting<'a> for StoredHpkePrivateKey {
    type EncryptedForm = StoredHpkePrivateKeyEncrypt<'a>;

    fn encrypt(&'a self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::EncryptedForm> {
        let sk = <Self as EncryptData>::encrypt_data(self, cipher, &self.sk)?;
        Ok(StoredHpkePrivateKeyEncrypt { sk, pk: &self.pk })
    }
}

#[derive(Deserialize)]
pub struct StoredHpkePrivateKeyDecrypt {
    sk: Vec<u8>,
    pk: Vec<u8>,
}

impl Decrypting<'static> for StoredHpkePrivateKeyDecrypt {
    type DecryptedForm = StoredHpkePrivateKey;

    fn decrypt(self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::DecryptedForm> {
        let primary_key = Sha256Hash::hash_from(&self.pk);
        let sk = <StoredHpkePrivateKey as DecryptData>::decrypt_data(cipher, &primary_key, &self.sk)?;
        Ok(StoredHpkePrivateKey { sk, pk: self.pk })
    }
}

impl Decryptable<'static> for StoredHpkePrivateKey {
    type DecryptableFrom = StoredHpkePrivateKeyDecrypt;
}
