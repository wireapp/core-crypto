use std::io::{Read, Write};

use async_trait::async_trait;
use rusqlite::{Row, params};

use crate::{
    CryptoKeystoreResult, Sha256Hash,
    connection::{DatabaseConnection, KeystoreDatabaseConnection, TransactionWrapper},
    entities::{StoredPskBundle, count_helper, count_helper_tx, delete_helper, get_helper, load_all_helper},
    traits::{
        BorrowPrimaryKey, Entity, EntityBase, EntityDatabaseMutation, EntityDeleteBorrowed, EntityGetBorrowed,
        PrimaryKey,
    },
};

impl StoredPskBundle {
    fn from_row(row: &Row<'_>) -> rusqlite::Result<Self> {
        let psk_id = row.get("psk_id")?;
        let psk = row.get("psK")?;
        Ok(Self { psk_id, psk })
    }
}

impl EntityBase for StoredPskBundle {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "mls_psk_bundles";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::PskBundle(self.into())
    }
}

impl PrimaryKey for StoredPskBundle {
    type PrimaryKey = Vec<u8>;

    fn primary_key(&self) -> Self::PrimaryKey {
        self.psk_id.clone()
    }
}

impl BorrowPrimaryKey for StoredPskBundle {
    type BorrowedPrimaryKey = [u8];

    fn borrow_primary_key(&self) -> &Self::BorrowedPrimaryKey {
        &self.psk_id
    }
}

#[async_trait]
impl Entity for StoredPskBundle {
    async fn get(conn: &mut Self::ConnectionType, key: &Self::PrimaryKey) -> CryptoKeystoreResult<Option<Self>> {
        Self::get_borrowed(conn, key.as_slice()).await
    }

    async fn count(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<u32> {
        count_helper::<Self>(conn).await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        load_all_helper::<Self, _>(conn, Self::from_row).await
    }
}

#[async_trait]
impl EntityGetBorrowed for StoredPskBundle {
    async fn get_borrowed(
        conn: &mut Self::ConnectionType,
        key: &Self::BorrowedPrimaryKey,
    ) -> CryptoKeystoreResult<Option<Self>> {
        get_helper::<Self, _>(conn, "id", key, Self::from_row).await
    }
}

#[async_trait]
impl<'a> EntityDatabaseMutation<'a> for StoredPskBundle {
    type Transaction = TransactionWrapper<'a>;

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        let mut stmt =
            tx.prepare_cached("INSERT OR REPLACE INTO mls_psk_bundles (id_sha256, psk_id, psk) VALUES (?, ?, ?)")?;
        stmt.execute(params![self.primary_key(), self.psk_id, self.psk])?;
        Ok(())
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        count_helper_tx::<Self>(tx).await
    }

    async fn delete(tx: &Self::Transaction, id: &Self::PrimaryKey) -> CryptoKeystoreResult<bool> {
        Self::delete_borrowed(tx, id.as_slice()).await
    }
}

#[async_trait]
impl<'a> EntityDeleteBorrowed<'a> for StoredPskBundle {
    async fn delete_borrowed(tx: &Self::Transaction, id: &Self::BorrowedPrimaryKey) -> CryptoKeystoreResult<bool> {
        delete_helper::<Self>(tx, "id", id).await
    }
}
