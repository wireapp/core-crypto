import {
    Database as DatabaseInternal,
    openDatabase,
    inMemoryDatabase,
    DatabaseKey,
} from "../browser/autogenerated/core_crypto_ffi";

/**
 * The database acting as a core crypto keystore.
 */
export class Database extends DatabaseInternal {
    // We have to override this method in order to have no references
    // to the internal database type in our documentation.
    /** @internal */
    constructor(database: Database) {
        super(database);
    }

    /**
     * Open or create a [Database].
     */
    public static async open(
        location: string,
        key: DatabaseKey
    ): Promise<Database> {
        const internal = await openDatabase(location, key);
        // this class inherits the passthrough constructor from `DatabaseInternal`, so calling it here
        // produces a `Database` instance instead of `DatabaseInternal`.
        // they're structurally equivalent, but technically have distinct types.
        return new Database(internal);
    }

    /**
     * Create an in-memory Database whose data will be lost when the instance is dropped.
     */
    public static async inMemory(key: DatabaseKey): Promise<Database> {
        const internal = await inMemoryDatabase(key);
        return new Database(internal);
    }

    // We have to override this method in order to have no references
    // to the internal database type in our documentation.
    static instanceOf(obj: unknown): obj is Database {
        return super.instanceOf(obj);
    }
}
