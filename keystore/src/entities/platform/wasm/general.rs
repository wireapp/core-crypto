use crate::{
    MissingKeyErrorKind,
    connection::KeystoreDatabaseConnection,
    entities::{ConsumerData, EntityBase, UniqueEntity},
    traits::{EntityBase as NewEntityBase, UniqueEntityImplementationHelper},
};

#[async_trait::async_trait(?Send)]
impl EntityBase for ConsumerData {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "consumer_data";

    fn to_missing_key_err_kind() -> MissingKeyErrorKind {
        MissingKeyErrorKind::ConsumerData
    }

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::ConsumerData(self.into())
    }
}

#[async_trait::async_trait(?Send)]
impl UniqueEntity for ConsumerData {
    fn content(&self) -> &[u8] {
        &self.content
    }

    fn set_content(&mut self, content: Vec<u8>) {
        self.content = content;
    }
}

impl NewEntityBase for ConsumerData {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "consumer_data";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::ConsumerData(self.into())
    }
}

impl UniqueEntityImplementationHelper for ConsumerData {
    fn new(content: Vec<u8>) -> Self {
        Self { content }
    }

    fn content(&self) -> &[u8] {
        &self.content
    }
}
