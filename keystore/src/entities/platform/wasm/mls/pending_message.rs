#[async_trait::async_trait(?Send)]
impl EntityBase for MlsPendingMessage {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "mls_pending_messages";

    fn to_missing_key_err_kind() -> MissingKeyErrorKind {
        MissingKeyErrorKind::MlsPendingMessages
    }

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::MlsPendingMessage(self.into())
    }
}

#[async_trait::async_trait(?Send)]
impl EntityTransactionExt for MlsPendingMessage {}

impl MlsPendingMessage {
    pub async fn find_all_by_conversation_id(
        conn: &mut <Self as EntityBase>::ConnectionType,
        conversation_id: &[u8],
        params: EntityFindParams,
    ) -> crate::CryptoKeystoreResult<Vec<Self>> {
        let storage = conn.storage();
        let id = JsValue::from(Uint8Array::from(conversation_id));
        storage
            .get_all_with_query(<Self as EntityBase>::COLLECTION_NAME, Some(id), Some(params))
            .await
    }

    pub async fn delete_by_conversation_id(
        tx: &<Self as EntityDatabaseMutation<'_>>::Transaction,
        conversation_id: &[u8],
    ) -> crate::CryptoKeystoreResult<bool> {
        tx.new_delete::<Self>(conversation_id).await
    }
}

#[async_trait::async_trait(?Send)]
impl Entity for MlsPendingMessage {
    fn id_raw(&self) -> &[u8] {
        self.foreign_id.as_slice()
    }

    async fn find_all(conn: &mut Self::ConnectionType, params: EntityFindParams) -> CryptoKeystoreResult<Vec<Self>> {
        let storage = conn.storage();
        storage
            .get_all(<Self as EntityBase>::COLLECTION_NAME, Some(params))
            .await
    }

    async fn find_one(_: &mut Self::ConnectionType, _: &StringEntityId) -> crate::CryptoKeystoreResult<Option<Self>> {
        panic!("Must not be called. The intended usage is to call MlsPendingMessage::find_all_by_conversation_id().")
    }

    async fn find_many(
        conn: &mut Self::ConnectionType,
        _ids: &[StringEntityId],
    ) -> crate::CryptoKeystoreResult<Vec<Self>> {
        // Plot twist: we always select ALL the persisted groups. Unsure if we want to make it a real API with selection
        conn.storage()
            .get_all(<Self as EntityBase>::COLLECTION_NAME, None)
            .await
    }

    async fn count(conn: &mut Self::ConnectionType) -> crate::CryptoKeystoreResult<usize> {
        conn.storage().count(<Self as EntityBase>::COLLECTION_NAME).await
    }

    fn id(&self) -> CryptoKeystoreResult<wasm_bindgen::JsValue> {
        Ok(js_sys::Uint8Array::from(self.foreign_id.as_slice()).into())
    }

    fn merge_key(&self) -> Vec<u8> {
        // Use this as a merge key because the `id` is not used as a primary key
        // but  as a foreign key: it's the ID of the PersistedMlsPendingGroup.
        self.message.clone()
    }

    fn encrypt(&mut self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<()> {
        self.message = <Self as Entity>::encrypt_data(self, cipher, self.message.as_slice())?;

        Ok(())
    }

    fn decrypt(&mut self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<()> {
        self.message = self.decrypt_data(cipher, self.message.as_slice())?;

        Ok(())
    }
}

