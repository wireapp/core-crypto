impl StoredCredential {
    fn load(
        transaction: &Transaction<'_>,
        rowid: i64,
        created_at: u64,
        ciphersuite: u16,
    ) -> CryptoKeystoreResult<Self> {
        let mut blob = transaction.blob_open(rusqlite::MAIN_DB, Self::COLLECTION_NAME, "session_id", rowid, true)?;
        let mut session_id = vec![];
        blob.read_to_end(&mut session_id)?;
        blob.close()?;

        let mut blob = transaction.blob_open(rusqlite::MAIN_DB, Self::COLLECTION_NAME, "credential", rowid, true)?;
        let mut credential = vec![];
        blob.read_to_end(&mut credential)?;
        blob.close()?;

        let mut blob = transaction.blob_open(rusqlite::MAIN_DB, Self::COLLECTION_NAME, "private_key", rowid, true)?;
        let mut private_key = vec![];
        blob.read_to_end(&mut private_key)?;
        blob.close()?;

        let mut blob = transaction.blob_open(rusqlite::MAIN_DB, Self::COLLECTION_NAME, "public_key", rowid, true)?;
        let mut public_key = vec![];
        blob.read_to_end(&mut public_key)?;
        blob.close()?;

        Ok(Self {
            session_id,
            credential,
            ciphersuite,
            created_at,
            public_key,
            private_key,
        })
    }

    fn from_row(row: &Row<'_>) -> rusqlite::Result<Self> {
        let session_id = row.get("session_id")?;
        let credential = row.get("credential")?;
        let created_at = row.get("created_at")?;
        let ciphersuite = row.get("ciphersuite")?;
        let public_key = row.get("public_key")?;
        let private_key = row.get("private_key")?;

        Ok(Self {
            session_id,
            credential,
            created_at,
            ciphersuite,
            public_key,
            private_key,
        })
    }
}

impl EntityBase for StoredCredential {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = u64;
    const COLLECTION_NAME: &'static str = "mls_credentials";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::StoredCredential(self.into())
    }
}

impl PrimaryKey for StoredCredential {
    type PrimaryKey = Sha256Hash;

    fn primary_key(&self) -> Self::PrimaryKey {
        Sha256Hash::hash_from(&self.public_key)
    }
}

#[async_trait]
impl Entity for StoredCredential {
    async fn get(conn: &mut Self::ConnectionType, key: &Self::PrimaryKey) -> CryptoKeystoreResult<Option<Self>> {
        let conn = conn.conn().await;
        let mut stmt = conn.prepare_cached(
            "SELECT
                session_id,
                credential,
                unixepoch(created_at) AS created_at,
                ciphersuite,
                public_key,
                private_key
            FROM mls_credentials
            WHERE public_key_sha256 = ?",
        )?;

        stmt.query_row([key], Self::from_row).optional().map_err(Into::into)
    }

    async fn count(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<u32> {
        count_helper::<Self>(conn).await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        let conn = conn.conn().await;
        let mut stmt = conn.prepare_cached(
            "SELECT
                session_id,
                credential,
                unixepoch(created_at) AS created_at,
                ciphersuite,
                public_key,
                private_key
            FROM mls_credentials",
        )?;
        stmt.query_map([], Self::from_row)?
            .collect::<Result<_, _>>()
            .map_err(Into::into)
    }
}

#[async_trait]
impl<'a> EntityDatabaseMutation<'a> for StoredCredential {
    type Transaction = TransactionWrapper<'a>;

    async fn pre_save(&mut self) -> CryptoKeystoreResult<Self::AutoGeneratedFields> {
        let now = SystemTime::now();
        let created_at = now
            .duration_since(SystemTime::UNIX_EPOCH)
            .map_err(|_| CryptoKeystoreError::TimestampError)?
            .as_secs();
        self.created_at = created_at;
        Ok(created_at)
    }

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        // note not "or replace": a duplicate credential is an error and will produce one in sql
        let mut stmt = tx.prepare_cached(
            "INSERT INTO mls_credentials (
                public_key_sha256,
                public_key,
                session_id,
                credential,
                created_at,
                ciphersuite,
                private_key
            ) VALUES (?, ?, ?, ?, datetime(?, 'unixepoch'), ?, ?)",
        )?;
        stmt.execute(params![
            self.primary_key(),
            self.public_key,
            self.session_id,
            self.credential,
            self.created_at,
            self.ciphersuite,
            self.private_key,
        ])?;

        Ok(())
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        count_helper_tx::<Self>(tx).await
    }

    async fn delete(tx: &Self::Transaction, id: &Self::PrimaryKey) -> CryptoKeystoreResult<bool> {
        delete_helper::<Self>(tx, "public_key_sha256", id).await
    }
}

#[async_trait]
impl<'a> SearchableEntity<CredentialFindFilters<'a>> for StoredCredential {
    async fn find_all_matching(
        conn: &mut Self::ConnectionType,
        filters: &CredentialFindFilters<'a>,
    ) -> CryptoKeystoreResult<Vec<Self>> {
        // if we know something unique to this credential, use a more-efficient search
        if let Some(hash) = filters.hash.or_else(|| filters.public_key.map(Sha256Hash::hash_from)) {
            return Self::get(conn, &hash).await.map(|optional| {
                optional
                    .into_iter()
                    .filter(|credential| credential.matches(filters))
                    .collect()
            });
        }

        let CredentialFindFilters {
            ciphersuite,
            earliest_validity,
            session_id,
            ..
        } = filters;

        let mut query = "SELECT
                session_id,
                credential,
                unixepoch(created_at) AS created_at,
                ciphersuite,
                public_key,
                private_key
            FROM mls_credentials
            WHERE (true OR :ciphersuite OR :created_at OR :session_id) "
            .to_owned();

        if ciphersuite.is_some() {
            query.push_str("AND ciphersuite = :ciphersuite ");
        }
        if earliest_validity.is_some() {
            query.push_str("AND unixepoch(created_at) = :created_at ");
        }
        if session_id.is_some() {
            query.push_str("AND session_id = :session_id ");
        }
        if session_id.is_some() {
            query.push_str("AND session_id = ?3 ");
        }

        let conn = conn.conn().await;
        let mut stmt = conn.prepare(&query)?;
        stmt.query_map(
            named_params![":ciphersuite": ciphersuite, ":created_at": earliest_validity, ":session_id": session_id],
            Self::from_row,
        )?
        .collect::<Result<_, _>>()
        .map_err(Into::into)
    }

    fn matches(
        &self,
        CredentialFindFilters {
            hash,
            public_key,
            ciphersuite,
            earliest_validity,
            session_id,
        }: &CredentialFindFilters<'a>,
    ) -> bool {
        hash.is_none_or(|hash| hash == self.primary_key())
            && public_key.is_none_or(|public_key| public_key == self.public_key)
            && session_id.is_none_or(|session_id| session_id == self.session_id)
            && ciphersuite.is_none_or(|ciphersuite| ciphersuite == self.ciphersuite)
            && earliest_validity.is_none_or(|earliest_validity| earliest_validity == self.created_at)
    }
}
