use async_trait::async_trait;
use serde::{Deserialize, Serialize};

use crate::{
    CryptoKeystoreResult,
    connection::{KeystoreDatabaseConnection, TransactionWrapper},
    entities::StoredPskBundle,
    traits::{
        BorrowPrimaryKey, DecryptData, Decryptable, Decrypting, EncryptData, Encrypting, Entity, EntityBase,
        EntityDatabaseMutation, EntityDeleteBorrowed, EntityGetBorrowed, PrimaryKey,
    },
};

impl EntityBase for StoredPskBundle {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "mls_psk_bundles";

    fn to_transaction_entity(self) -> crate::transaction::dynamic_dispatch::Entity {
        crate::transaction::dynamic_dispatch::Entity::PskBundle(self.into())
    }
}

impl PrimaryKey for StoredPskBundle {
    type PrimaryKey = Vec<u8>;

    fn primary_key(&self) -> Vec<u8> {
        self.psk_id.clone()
    }
}

impl BorrowPrimaryKey for StoredPskBundle {
    type BorrowedPrimaryKey = [u8];

    fn borrow_primary_key(&self) -> &[u8] {
        &self.psk_id
    }
}

#[async_trait(?Send)]
impl Entity for StoredPskBundle {
    async fn get(conn: &mut Self::ConnectionType, key: &Vec<u8>) -> CryptoKeystoreResult<Option<Self>> {
        Self::get_borrowed(conn, key.as_slice()).await
    }

    async fn count(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<u32> {
        conn.storage().count::<Self>().await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        conn.storage().get_all().await
    }
}

#[async_trait(?Send)]
impl EntityGetBorrowed for StoredPskBundle {
    async fn get_borrowed(
        conn: &mut <Self as EntityBase>::ConnectionType,
        key: &[u8],
    ) -> CryptoKeystoreResult<Option<Self>> {
        conn.storage().get(key).await
    }
}

#[async_trait(?Send)]
impl<'a> EntityDatabaseMutation<'a> for StoredPskBundle {
    type Transaction = TransactionWrapper<'a>;

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        tx.save(self).await
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        tx.count::<Self>().await
    }

    async fn delete(tx: &Self::Transaction, id: &Vec<u8>) -> CryptoKeystoreResult<bool> {
        Self::delete_borrowed(tx, id.as_slice()).await
    }
}

#[async_trait(?Send)]
impl<'a> EntityDeleteBorrowed<'a> for StoredPskBundle {
    async fn delete_borrowed(
        tx: &<Self as EntityDatabaseMutation<'a>>::Transaction,
        id: &[u8],
    ) -> CryptoKeystoreResult<bool> {
        tx.delete::<Self>(id).await
    }
}

#[derive(Serialize)]
pub struct StoredPskBundleEncrypt<'a> {
    psk_id: &'a [u8],
    psk: Vec<u8>,
}

impl<'a> Encrypting<'a> for StoredPskBundle {
    type EncryptedForm = StoredPskBundleEncrypt<'a>;

    fn encrypt(&'a self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::EncryptedForm> {
        let psk = <Self as EncryptData>::encrypt_data(self, cipher, &self.psk)?;
        Ok(StoredPskBundleEncrypt {
            psk_id: &self.psk_id,
            psk,
        })
    }
}

#[derive(Deserialize)]
pub struct StoredPskBundleDecrypt {
    psk_id: Vec<u8>,
    psk: Vec<u8>,
}

impl Decrypting<'static> for StoredPskBundleDecrypt {
    type DecryptedForm = StoredPskBundle;

    fn decrypt(self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::DecryptedForm> {
        let psk = <StoredPskBundle as DecryptData>::decrypt_data(cipher, &self.psk_id, &self.psk)?;
        Ok(StoredPskBundle {
            psk_id: self.psk_id,
            psk,
        })
    }
}

impl Decryptable<'static> for StoredPskBundle {
    type DecryptableFrom = StoredPskBundleDecrypt;
}
