use async_trait::async_trait;
use serde::{Deserialize, Serialize};

use crate::{
    CryptoKeystoreResult,
    connection::{KeystoreDatabaseConnection, TransactionWrapper},
    entities::ProteusPrekey,
    traits::{
        DecryptData, Decryptable, Decrypting, EncryptData, Encrypting, Entity, EntityBase, EntityDatabaseMutation,
        KeyType, PrimaryKey,
    },
};

impl EntityBase for ProteusPrekey {
    type ConnectionType = KeystoreDatabaseConnection;
    type AutoGeneratedFields = ();
    const COLLECTION_NAME: &'static str = "proteus_prekeys";
}

impl PrimaryKey for ProteusPrekey {
    type PrimaryKey = u16;
    fn primary_key(&self) -> Self::PrimaryKey {
        self.id
    }
}

#[async_trait(?Send)]
impl Entity for ProteusPrekey {
    async fn get(conn: &mut Self::ConnectionType, key: &u16) -> CryptoKeystoreResult<Option<Self>> {
        conn.storage().get(key.bytes().as_ref()).await
    }

    async fn count(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<u32> {
        conn.storage().count::<Self>().await
    }

    async fn load_all(conn: &mut Self::ConnectionType) -> CryptoKeystoreResult<Vec<Self>> {
        conn.storage().get_all().await
    }
}

#[async_trait(?Send)]
impl<'a> EntityDatabaseMutation<'a> for ProteusPrekey {
    type Transaction = TransactionWrapper<'a>;

    async fn save(&'a self, tx: &Self::Transaction) -> CryptoKeystoreResult<()> {
        tx.save(self).await
    }

    async fn count(tx: &Self::Transaction) -> CryptoKeystoreResult<u32> {
        tx.count::<Self>().await
    }

    async fn delete(tx: &Self::Transaction, id: &u16) -> CryptoKeystoreResult<bool> {
        tx.delete::<Self>(id.bytes().as_ref()).await
    }
}

#[derive(Serialize, Deserialize)]
pub struct ProteusPrekeyEncrypted<'a> {
    #[serde(skip)]
    _lifetime: std::marker::PhantomData<&'a ()>,
    id: u16,
    prekey: Vec<u8>,
}

impl<'a> Encrypting<'a> for ProteusPrekey {
    type EncryptedForm = ProteusPrekeyEncrypted<'a>;

    fn encrypt(&'a self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::EncryptedForm> {
        let prekey = <Self as EncryptData>::encrypt_data(self, cipher, &self.prekey)?;
        Ok(ProteusPrekeyEncrypted {
            _lifetime: std::marker::PhantomData,
            id: self.id,
            prekey,
        })
    }
}

impl Decrypting<'static> for ProteusPrekeyEncrypted<'static> {
    type DecryptedForm = ProteusPrekey;

    fn decrypt(self, cipher: &aes_gcm::Aes256Gcm) -> CryptoKeystoreResult<Self::DecryptedForm> {
        let prekey = <ProteusPrekey as DecryptData>::decrypt_data(cipher, &self.id, &self.prekey)?;
        Ok(ProteusPrekey::from_raw(self.id, prekey))
    }
}

impl Decryptable<'static> for ProteusPrekey {
    type DecryptableFrom = ProteusPrekeyEncrypted<'static>;
}
